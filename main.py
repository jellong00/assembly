"""
main.py — 국회의원 본회의 표결행태 분석 대시보드 (소개 페이지)
"""

import streamlit as st

st.set_page_config(
    page_title="국회의원 본회의 표결행태 분석 대시보드",
    layout="wide",
    initial_sidebar_state="expanded",
)

st.title("국회의원 본회의 표결행태 분석 대시보드")

st.markdown("""
이 대시보드는 **열린국회정보 포털**에서 다운로드한 **22대 국회 본회의 표결정보** 데이터를 활용해
국회의원과 정당의 표결 행태를 탐색적으로 분석합니다.
""")

st.subheader("분석 목적")
st.markdown("""
- 22대 국회 본회의 표결 현황 파악 (월별 추이 포함)
- 정당별 찬성·반대·기권 비율 및 표결 참여율 분석
- 의원별 표결 참여 현황과 소속 정당 다수 입장과의 일치율 분석
- 여야 간 표결 갈등도가 높은 의안, 초당적 합의가 높은 의안 탐색
- 특정 의안에 대한 의원별 표결 결과 상세 조회
""")

st.subheader("사용 데이터")
st.markdown("""
| 파일 | 용도 |
|---|---|
| `data/vote_info.csv` | 의원별 본회의 표결 결과 (찬성/반대/기권) — 핵심 데이터 |
| `data/bill_detail.csv` | 의안 제안일, 소관위원회, 본회의 심의결과 등 상세정보 |
| `data/bill_proposer.csv` | 의안별 제안자 정보 |
| `data/bill_vote_summary.csv` | 의안 단위 표결 집계 (재적의원수·총투표수·찬성/반대/기권 수) |
| `data/member_info.csv` | 국회의원 선거구, 소속 위원회, 재선·당선 이력 등 인적사항 |

⚠️ 이 대시보드는 **실시간 API 호출을 하지 않습니다.** 열린국회정보 Open API 서버가
Streamlit Cloud(해외 서버 IP)와의 연결을 지속적으로 차단하는 문제가 있어,
로컬(국내 네트워크)에서 미리 데이터를 내려받아 CSV 스냅샷으로 저장한 뒤 이를 읽는 방식으로 전환했습니다.
""")

st.subheader("데이터 갱신 방식")
st.markdown("""
- 위 CSV 파일들은 **특정 시점에 다운로드한 스냅샷**입니다. 이후 새로 발의·표결된 의안은 반영되지 않습니다.
- 데이터를 최신화하려면, 열린국회정보 포털에서 파일을 다시 받아 `data/` 폴더의 CSV를 교체하고
  GitHub에 다시 push해야 합니다.
- 앱 실행 시 `@st.cache_data`로 한 번만 로드되며, 이후에는 캐시된 데이터를 사용합니다.
""")

st.subheader("페이지 안내")
st.markdown("""
- **01. 전체 표결현황**: 월별 표결 의안 수 추이, 표결 결과 분포, 최근 표결 의안 목록
- **02. 정당별 표결분석**: 정당별 찬성/반대/기권 비율, 표결 참여율, 여야 갈등도·초당적 합의 상위 의안
- **03. 의원별 표결분석**: 의원별 참여 현황, 소속 정당 다수 입장과의 일치율, 표결 유사 의원 찾기
- **04. 의안별 상세분석**: 특정 의안의 제안 정보, 정당별·의원별 표결 결과 상세
""")

st.subheader("⚠️ 데이터 해석 시 주의사항")
st.warning("""
- 22대 국회 기준 데이터이며, 기록표결(전자표결) 자료라 **모든 본회의 안건을 대표하지 않을 수 있습니다** (구두 표결 등 미포함 가능).
- 이 데이터셋에는 '불참(결석)' 기록이 없습니다. 표결결과는 찬성/반대/기권만 존재하며,
  각 페이지의 '참여율'은 (실제 표결 건수) / (해당 정당 소속 의원수 × 전체 의안 수)로 계산한 근사치입니다.
- '정당 다수 입장과 다른 표결'은 공식 당론 자료 없이 계산한 통계적 개념으로, **'당론 위반'을 의미하지 않습니다.**
- 여당/야당 구분은 데이터에 없어 표결일 기준으로 직접 설정한 여야 이력 매핑을 사용하며, 공식 자료가 아닙니다.
- 모든 분석은 **기술통계 및 탐색적 분석**이며, 인과관계를 나타내지 않습니다.
""")

st.info("👈 왼쪽 사이드바에서 분석하고 싶은 페이지를 선택하세요.")
