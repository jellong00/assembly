import streamlit as st

st.set_page_config(page_title="국회 표결 분석 대시보드", page_icon="🏛️", layout="wide")

st.title("🏛️ 국회의원 본회의 표결 분석 대시보드")

st.markdown("""
### 프로젝트 개요

**질문** : 정당 소속이 국회의원 개인의 표결 행태(찬반 일치도)를 얼마나 설명하는가?

**데이터** : 열린국회정보 Open API
- 국회의원 본회의 표결정보
- 의안정보 통합 API (ALLBILLV2)
- 국회의원 인적사항 통합 API (ALLNAMEMBER)

**분석 방법** : 의안 × 정당(표결 당시 기준)별 다수결 방향을 계산하고,
개별 의원의 표결이 이와 일치하는 비율("정당 일치율")을 산출합니다.

👈 왼쪽 사이드바에서 **01_대시보드** 페이지로 이동해서 분석 결과를 확인하세요.
""")

with st.expander("⚠️ 데이터 한계 및 주의사항", expanded=False):
    st.markdown("""
    - 정당 일치율은 표결 시점의 정당(표결정보 API의 `POLY_NM`)을 기준으로 계산했습니다.
      따라서 의원 개인의 정당 이동(탈당/입당) 이력과 무관하게, **그 표결 당시** 소속
      정당 노선과 일치했는지를 정확히 측정합니다.
    - 반면 재선여부·위원회 등 인적사항 데이터는 **조회 시점(최신) 기준**입니다.
      의원 이동 이력이 있으면 "현재 소속"과 "표결 당시 소속"이 다를 수 있어요.
    - 동명이인 문제를 피하기 위해 의원코드(`MONA_CD`)가 있는 경우 이를 기준으로 조인했습니다.
    - API 특성상 표결정보는 의안(BILL_ID) 단위로만 조회할 수 있어, 분석 대상 의안 수를
      사이드바에서 제한했습니다 (전수조사가 아님).
    """)
