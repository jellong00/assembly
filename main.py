"""
main.py — 국회의원 본회의 표결행태 분석 대시보드 (소개 페이지)
"""

import streamlit as st

st.set_page_config(
    page_title="국회의원 본회의 표결행태 분석 대시보드",
    layout="wide",
    initial_sidebar_state="expanded",
)

st.title("국회의원 본회의 표결행태 분석 대시보드")

st.markdown("""
이 대시보드는 **열린국회정보 Open API**의 본회의 표결정보를 활용해
국회의원과 정당의 표결 행태를 탐색적으로 분석합니다.
""")

st.subheader("분석 목적")
st.markdown("""
- 국회대수별 본회의 표결 현황 파악
- 정당별 찬성·반대·기권 비율 및 표결 결집도 분석
- 의원별 표결 참여 현황과 소속 정당 다수 입장과의 일치율 분석
- 여야 간 표결 갈등도가 높은 의안, 초당적 합의가 높은 의안 탐색
- 특정 의안에 대한 의원별 표결 결과 상세 조회
""")

st.subheader("사용 API")
st.markdown("""
| API | 용도 | 필수 파라미터 |
|---|---|---|
| 의안정보 통합 API (ALLBILLV2) | 의안 목록 및 BILL_ID 확보 | ERACO(대수) |
| 국회의원 본회의 표결정보 | 의원별 찬성/반대/기권 결과 | AGE(대수), BILL_ID |
| 의안 상세정보 (BILLINFODETAIL) | 제안일, 소관위, 처리결과 등 | BILL_ID |
| 의안 제안자정보 (BILLINFOPPSR) | 의안 제안자 목록 | BILL_ID |
| 국회의원 인적사항 | 위원회, 재선여부, 당선대수 등 | (전부 선택) |

⚠️ 표결정보 · 상세정보 · 제안자정보는 **BILL_ID가 있어야만 조회 가능**합니다.
따라서 이 대시보드는 항상 **① 의안 목록(ALLBILLV2) 조회 → ② 확보한 BILL_ID로 표결정보 등을 반복 조회**
하는 순서로 데이터를 수집합니다. 조회 의안 수가 많으면 시간이 걸릴 수 있어,
각 페이지 사이드바에서 한 세션에 조회할 의안 수를 제한할 수 있습니다.
""")

st.subheader("데이터 갱신 방식")
st.markdown("""
- 매 조회 시 Open API를 실시간 호출하며, `st.cache_data`로 최대 1시간 캐싱합니다.
- API 키가 없거나 호출이 실패해도 앱이 종료되지 않도록, 사이드바에서 **샘플 데이터**로
  전환해 기능을 시연할 수 있습니다. (샘플 데이터는 실제 통계가 아닙니다.)
""")

st.subheader("페이지 안내")
st.markdown("""
- **01. 전체 표결현황**: 국회대수·기간별 전체 표결 통계, 표결 결과 분포, 최근 표결 의안 목록
- **02. 정당별 표결분석**: 정당별 찬성/반대/기권 비율, 결집도(다수비율·Rice Index), 여야 갈등도·초당적 합의 상위 의안
- **03. 의원별 표결분석**: 의원별 참여 현황, 소속 정당 다수 입장과의 일치율, 표결 유사 의원 찾기
- **04. 의안별 상세분석**: 특정 의안의 제안 정보, 정당별·의원별 표결 결과 상세
""")

st.subheader("API 키 설정 방법")
st.code(
    'OPEN_ASSEMBLY_API_KEY = "발급받은 API 키"',
    language="toml",
)
st.caption("`.streamlit/secrets.toml` (로컬) 또는 Streamlit Cloud > App settings > Secrets 에 위와 같이 등록하세요.")

st.subheader("⚠️ 데이터 해석 시 주의사항")
st.warning("""
- 기록표결(전자표결) 자료는 **모든 본회의 안건을 대표하지 않을 수 있습니다** (구두 표결 등 미포함 가능).
- '불참'은 표결 결과값 자체이며, API 응답 누락과는 다릅니다. 응답이 없다고 해서 자동으로 불참으로 간주하지 않습니다.
- '정당 다수 입장과 다른 표결'은 공식 당론 자료 없이 계산한 통계적 개념으로, **'당론 위반'을 의미하지 않습니다.**
- 여당/야당 구분은 API에 없어 사용자가 직접 설정한 매핑을 기준으로 하며, 공식 자료가 아닙니다.
- 모든 분석은 **기술통계 및 탐색적 분석**이며, 인과관계를 나타내지 않습니다.
- 의안 목록(ALLBILLV2)에는 법률안 외에 '의사일정 변경동의' 등 절차적 안건이 포함되어 있어,
  정책적 표결 분석 시 의안종류(BILL_KND) 필터링을 권장합니다.
""")

st.info("👈 왼쪽 사이드바에서 분석하고 싶은 페이지를 선택하세요.")
